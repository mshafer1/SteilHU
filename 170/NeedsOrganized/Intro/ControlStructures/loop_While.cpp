//this program introduces the while loop

#include<iostream>

using std::cout;
using std::cin;
using std::endl;

int main()
{
	//	there are 4 parts to every loop
	//	1	initialize the test condition
	//	2	test the condition
	//	3	do some useful things
	//	4	change the test condition (go back to step 2)

	int i = 0;

	while( i < 1000 )
	{
		cout << i << " I will not talk in class." << endl;
		i++;
	}
	cin.get();
	return 0;
}
