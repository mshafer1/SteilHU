//this program explores int and float types

#include <iostream>

using std::cout;
using std::cin;
using std::endl;

int main()
{
	int i = 50; //integer
	float f = 100.25; //floating point

	cout << "i = " << i << endl;
	cout << "f = " << f << endl;

	i = f;
	cout << "i = f;\n    i = " << i << endl;

	f = 1/2;
	cout << "\nf = 1/2;\n    f = " << f << endl;

	f = 1.0/2.0;
	cout << "\nf = 1.0/2.0;\n    f = " << f << endl;

	f = 1/2.0;
	cout << "\nf = 1/2.0;\n    f = " << f << endl;
	
	f = 3.5;
	i = 2;

	f = f/i;
	cout << "\nf = 3.5;\ni = 2;\nf = f/i;\n    f = " << f << endl;

	f = 1.25;
	i = 2;

	f = i * f;
	cout << "\nf = 1.25;\ni = 2;\nf = i * f;\n    f = " << f << endl;

	f = i + f;
	cout << "\nf = i + f;\n    f = " << f << endl;

	f = 1 + 2;
	cout << "\nf = 1 + 2;\n    f = " << f << endl;

    cin.get();
    return 0;
}
